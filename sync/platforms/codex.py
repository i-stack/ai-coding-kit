import re
from pathlib import Path
from typing import Any

from .common import (
    codex_config_path,
    codex_generated_toml_path,
    env_for_platform,
    mcp_servers,
    platform_config,
    toml_array,
    toml_header_key_segment,
    toml_inline_table,
    toml_quote,
    toml_value,
    xcode_codex_dir,
)

MCP_BEGIN = "# BEGIN MCP SYNC (from env/config.json)"
MCP_END = "# END MCP SYNC"
MCP_BLOCK_PATTERN = re.compile(
    r"# BEGIN MCP SYNC(?: \(from [^)]+\))?"
    + r".*?"
    + re.escape(MCP_END),
    re.DOTALL,
)

SHARED_BEGIN = "# BEGIN CODEX SHARED (from env/config.json)"
SHARED_END = "# END CODEX SHARED"
SHARED_BLOCK_PATTERN = re.compile(
    r"# BEGIN CODEX SHARED(?: \(from [^)]+\))?"
    + r".*?"
    + re.escape(SHARED_END),
    re.DOTALL,
)


def generate_mcp_toml(servers: dict[str, Any]) -> str:
    lines: list[str] = ["# AUTOGENERATED from env/config.json", ""]
    for name, raw_cfg in servers.items():
        if not isinstance(raw_cfg, dict):
            raise ValueError(f"mcpServers.{name} must be an object.")
        cfg = dict(raw_cfg)
        key = toml_header_key_segment(name)
        lines.append(f"[mcp_servers.{key}]")
        if "url" in cfg:
            lines.append(f"url = {toml_quote(str(cfg['url']))}")
            if "headers" in cfg and isinstance(cfg["headers"], dict):
                headers = {k: str(v) for k, v in cfg["headers"].items()}
                lines.append(f"headers = {toml_inline_table(headers)}")
        else:
            lines.append(f"command = {toml_quote(str(cfg['command']))}")
            if "args" in cfg and isinstance(cfg["args"], list):
                lines.append(f"args = {toml_array(cfg['args'])}")
        env = cfg.get("env") or {}
        if isinstance(env, dict) and env:
            values = {k: str(v) for k, v in env.items()}
            lines.append(f"env = {toml_inline_table(values)}")
        lines.append("")
    return "\n".join(lines).rstrip()


def generate_codex_shared_toml(data: dict[str, Any]) -> str:
    codex = platform_config(data, "codex")
    codex_env = env_for_platform(data, "codex")

    model_provider = codex.get("modelProvider")
    use_custom_provider = isinstance(model_provider, str) and model_provider != ""

    model = codex.get("model")

    base_url = codex.get("openaiBaseUrl") or codex_env.get("OPENAI_BASE_URL")
    provider_name = codex.get("providerName", model_provider or "custom")
    wire_api = codex.get("wireApi", "responses")
    env_key = codex.get("envKey", "OPENAI_API_KEY")

    lines: list[str] = ["# AUTOGENERATED from env/config.json"]
    if model:
        lines.append(f"model = {toml_quote(str(model))}")
    if use_custom_provider:
        lines.append(f"model_provider = {toml_quote(str(model_provider))}")
    if effort := codex.get("modelReasoningEffort"):
        lines.append(f"model_reasoning_effort = {toml_quote(str(effort))}")

    if use_custom_provider and base_url:
        lines.extend(
            [
                "",
                f"[model_providers.{toml_header_key_segment(str(model_provider))}]",
                f"name = {toml_quote(str(provider_name))}",
                f"base_url = {toml_quote(str(base_url))}",
                f"env_key = {toml_quote(str(env_key))}",
                f"wire_api = {toml_quote(str(wire_api))}",
            ]
        )

    features = codex.get("features")
    if isinstance(features, dict) and features:
        lines.extend(["", "[features]"])
        for key, value in features.items():
            lines.append(f"{key} = {toml_value(value)}")

    projects = codex.get("projects")
    if isinstance(projects, dict) and projects:
        for path, cfg in projects.items():
            if not isinstance(cfg, dict):
                raise ValueError(f"platforms.codex.projects.{path} must be an object.")
            lines.extend(["", f"[projects.{toml_header_key_segment(str(path))}]"])
            for key, value in cfg.items():
                lines.append(f"{key} = {toml_value(value)}")

    return "\n".join(lines).rstrip()


def merge_codex_managed_blocks(cfg: Path, shared_body: str, mcp_body: str) -> None:
    shared_block = f"{SHARED_BEGIN}\n{shared_body}\n{SHARED_END}" if shared_body else ""
    mcp_block = f"{MCP_BEGIN}\n{mcp_body}\n{MCP_END}"
    region = (shared_block + "\n\n" + mcp_block) if shared_block else mcp_block
    region = f"\n{region}\n"

    if not cfg.exists():
        cfg.parent.mkdir(parents=True, exist_ok=True)
        cfg.write_text(
            f"# Codex config\n# Add host-specific settings outside managed blocks.\n{region}",
            encoding="utf-8",
        )
        print(f"Created {cfg} with managed blocks.")
        return

    text = cfg.read_text(encoding="utf-8")
    text = SHARED_BLOCK_PATTERN.sub("", text)
    text = MCP_BLOCK_PATTERN.sub("", text)

    table_re = re.compile(r"^\[\[?[^\]\n]+\]\]?\s*$", re.MULTILINE)
    match = table_re.search(text)
    if match:
        head = text[: match.start()].rstrip()
        tail = text[match.start():]
        new_text = (head + "\n" + region + "\n" + tail) if head else (region + "\n" + tail)
        action = "Inserted managed blocks before first [table]"
    else:
        new_text = text.rstrip() + "\n" + region
        action = "Appended managed blocks"

    cfg.write_text(new_text, encoding="utf-8")
    print(f"{action} in {cfg}.")


def sync(data: dict[str, Any]) -> None:
    servers = mcp_servers(data)
    generated = generate_mcp_toml(servers)
    shared = generate_codex_shared_toml(data)

    out = codex_generated_toml_path()
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(generated, encoding="utf-8")
    print(f"Wrote: {out}")
    merge_codex_managed_blocks(codex_config_path(), shared, generated)

    xc = xcode_codex_dir()
    xc.mkdir(parents=True, exist_ok=True)
    xc_gen = xc / "mcp.generated.toml"
    xc_gen.write_text(generated, encoding="utf-8")
    print(f"Wrote: {xc_gen}")
    merge_codex_managed_blocks(xc / "config.toml", shared, generated)
